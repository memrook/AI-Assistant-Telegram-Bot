            if len(response) < 300:
                keyboard = [[InlineKeyboardButton("–†–∞–∑–≤–µ—Ä–Ω—É—Ç—ã–π –æ—Ç–≤–µ—Ç", callback_data="detailed_answer")]]
                reply_markup = InlineKeyboardMarkup(keyboard)
                await processing_message.edit_text(response, reply_markup=reply_markup)
